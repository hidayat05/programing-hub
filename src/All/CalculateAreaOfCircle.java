public class CalculateAreaOfCircle {
    public static void main(String[] args) {
        int radius = 3;
        System.out.println("The Radius of circle is : " + radius);

        double area = Math.PI * radius * radius;
        System.out.println("area of circle is : " + area);
    }
}
